StartupEvents.registry("sound_event",rt=>{
    rt.create("hannibal_delight:music.faded")
})
StartupEvents.registry("item",r =>{
    r.create("hannibal_delight:raw_human_meat") 
        .food(et=>{
            et.hunger(5)
            et.saturation(0.2)
            et.meat()
            et.alwaysEdible()
            et.effect("minecraft:strength",20*10,1,0.8)
            et.effect("minecraft:wither",20*20,4,0.2)
        })
    r.create("hannibal_delight:cooked_human_meat")
    .food(et=>{
        et.hunger(10)
        et.saturation(0.7)
        et.meat()
        et.alwaysEdible()
        et.effect("minecraft:strength",20*10,1,0.8)
        et.effect("minecraft:wither",20*10,4,0.02)
    })
    r.create("hannibal_delight:raw_human_meat_slice")
    .food(et=>{
        et.hunger(2)
        et.saturation(0.2)
        et.meat()
        et.alwaysEdible()
        et.fastToEat()
        et.effect("minecraft:strength",20*5,1,0.8)
        et.effect("minecraft:wither",20*12,4,0.2)
    })
    r.create("hannibal_delight:cooked_human_meat_slice")
    .food(et=>{
        et.hunger(5)
        et.saturation(0.7)
        et.meat()
        et.alwaysEdible()
        et.fastToEat()
        et.effect("minecraft:strength",20*5,1,0.8)
        et.effect("minecraft:wither",20*6,4,0.02)
    })
})
